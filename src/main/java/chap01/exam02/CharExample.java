package chap01.exam02;

public class CharExample {
    public static void main(String[] args) {
        char c1 = 'A'; // 유니코드 0x0041
        char c2 = 65;
        char c3 = '\u0041'; // 유니코드 41번

        System.out.println(c1);
        System.out.println(c2);
        System.out.println(c3);
    }
}
