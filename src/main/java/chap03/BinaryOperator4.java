package chap03;

public class BinaryOperator4 {
    public static void main(String[] args) {
        // 문자열 연결 연산+ , 앞에서부터 정해짐
        System.out.println("a" + "b" + "c");
        System.out.println(1 + 2);
        System.out.println("1" + 2); // 12자체가 문자
        System.out.println(2 + "1" + 2 + 3);
        System.out.println(2 + 3 + 3 + "1");
        System.out.println(2 + 3 + 3 + "1" + 2 + 3);


    }


}
